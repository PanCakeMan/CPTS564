
public class Passenger {

	public int start;
	public int end;
	public String ride;
	public String route;
	public boolean needbus;

	public Passenger(int start, int end, String ride, String route, boolean needBus) {

		this.start = start;
		this.end = end;
		this.ride = ride;
		this.route = route;
		this.needbus = needBus;

	}

}
